// Flatten all images into a single array with scale values
export const getAllImages = () => {
  const scales = [4, 5, 6, 8, 9];
  const images = [];

  imageCategories.forEach((category) => {
    category.images.forEach((img, idx) => {
      images.push({
        src: `/public/${img}`,
        srcPath: img,
        title: category.title,
        description: category.description,
        scale: scales[idx % scales.length],
      });
    });
  });

  return images;
};

// Alternative: just get all image paths
export const getAllImagePaths = () => {
  return imageCategories.flatMap((cat) =>
    cat.images.map((img) => ({
      path: img,
      category: cat.title,
      description: cat.description,
    })),
  );
};
